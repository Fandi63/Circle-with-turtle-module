import turtle
value = None ## Hodnota = žádná
while value == None: ## Dokud je hodnota žádná tak:
    turtle.speed(0) ## Nastav rychlost želvy na 0
    turtle.title("Circler v.1.0") ## Přepiš název okna na Circler v.1.0
    turtle.right(10) ## Desetkrát zahni doprava
    turtle.forward(10) ## Desetkrát běž dopředu
